package ex_11_WhileLoop;

public class Lab109_WhileLoop {
    public static void main(String[] args) {

        //using while loop
        int i=0;
        while(i<=3)
        {
            System.out.println(i);
            i++;
        }
        System.out.println("__________________________________________");
        //Using for loop
        for (int j = 0; j <= 3; j++) {
            System.out.println(j);
        }
    }
}
