package ex_09_SwitchCase;

public class Lab0092_interview {
    public static void main(String[] args) {
        char code='C';
        switch(code){
            default:
                System.out.println("default");

            case 'A':
                System.out.println("65");
                break;

            case 'B':
                System.out.println("66");
                break;
        }

    }
}
